class Profile {
  constructor({
    name,
    title,
    about,
    experience,
    education,
    skills,
    connections,
    averageRoleDuration,
  }) {
    this.name = name;
    this.title = title;
    this.about = about;
    this.experience = experience;
    this.education = education;
    this.skills = skills;
    this.connections = connections;
    this.averageRoleDuration = averageRoleDuration;
  }
}
